#UPDATE THE PRICE OF A SPECIFIC PRODUCT
UPDATE PRODUCT SET PRICE=200 WHERE PRODUCT_ID=2;

#DELETE A PRODUCT IF IT'S OUT OF STOCK
DELETE FROM PRODUCT WHERE STOCK= 0;

#RETRIEVE PRODUCT WHOSE PRICE BETWEEN 200 AND 2000
SELECT * FROM PRODUCT WHERE PRICE BETWEEN 200 AND 2000;

#RETRIEVE THE MOST EXPENSIVE USING MAX.
SELECT MAX(PRICE) AS EXPENSIVE FROM PRODUCT; 

#RETRIEVE THE MOST CHEAPEST USING MAX.
SELECT MIN(PRICE) AS CHEAPEST FROM PRODUCT;


