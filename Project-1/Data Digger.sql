#PRODUCT TABLE 
CREATE TABLE PRODUCT(
   PRODUCT_ID INT PRIMARY KEY,
   PRODUCT_NAME VARCHAR(15),
   PRICE INT,
   STOCK INT );

#INSERT AT LEAST 5 SAMPLE PRODUCT INTO THE PRODUCT TABLE
